// ─── Signals ──────────────────────────────────────────────────────────────────
import { useState, useEffect, useCallback } from "react";
import { NavLink, useSearchParams } from "react-router-dom";
import { C } from "../constants.jsx";
import { Card, SectionTitle, Grid, Btn, Sel, FG, Pill } from "../shared/Shared.jsx";
import { fp, f1 } from "../utils/utils.js";
import { CandleChart1, ConfRing, SigCard } from "../components/Charts.jsx";
import { PAIRS, FOREX_PAIRS, TFS } from "../components/Charts.jsx";
import { ChartWrap, Badge, ErrBox } from "../shared/Shared.jsx";
import { WS_BASE } from "../api/Api.jsx";
import { useLiveSocket } from "../hooks/useLiveSocket.js";

const SIGNALS_WS_URL = `${WS_BASE}/ws/signals`;

export default function Signals({ api }) {
  const [searchParams] = useSearchParams();
  const [lastCopied, setLastCopied] = useState(null); // { pair, direction } — shown as a "view in Copy Trading" banner
  const [pair,    setPair]    = useState("EURUSD");
  const [tf,      setTf]      = useState("H1");
  const [sigs,    setSigs]    = useState([]);
  const [sel,     setSel]     = useState(null);
  const [bars,    setBars]    = useState([]);
  const [busy,    setBusy]    = useState(false);
  const [bulkP,   setBulkP]   = useState(["EURUSD","GBPUSD","USDJPY","GBPJPY"]);
  const [bulkTf,  setBulkTf]  = useState(["H1","H4"]);
  const [minConf, setMinConf] = useState(0);
  const [dirF,    setDirF]    = useState("ALL");
  const [subTab,  setSubTab]  = useState("gen"); // gen | history
  const [hPair,   setHPair]   = useState("EURUSD");
  const [hTf,     setHTf]     = useState("H1");
  const [hPer,    setHPer]    = useState("1M");
  const [hData,   setHData]   = useState(null);
  const [hBusy,   setHBusy]   = useState(false);
  const [newIds,  setNewIds]  = useState(new Set());
  const [copiedIds, setCopiedIds] = useState(new Set());
  const [copyBusy,  setCopyBusy]  = useState(null);
  const [copyErr,   setCopyErr]   = useState("");
  const [executeLive, setExecuteLive] = useState(false);
  const [bridgeReady, setBridgeReady] = useState(false);
  const [usage, setUsage] = useState(null);
  const [genErr, setGenErr] = useState("");

  const loadUsage = useCallback(() => {
    api.get("/account/usage").then(setUsage).catch(() => {});
  }, [api]);

  useEffect(() => {
    api.get("/bridge/status").then((d) => setBridgeReady(!!d.has_token)).catch(() => {});
    loadUsage();
  }, [api, loadUsage]);

  const copySignal = async (sigId, sigObj) => {
    setCopyBusy(sigId); setCopyErr(""); setLastCopied(null);
    try {
      const res = await api.post(`/signals/${sigId}/copy`, { lot_size: 0.02, risk_pct: 2, execute_live: executeLive && bridgeReady });
      setCopiedIds((prev) => new Set(prev).add(sigId));
      setLastCopied({ pair: res.pair || sigObj?.pair, direction: res.direction || sigObj?.direction, mode: res.execution_mode });
      loadUsage();
    } catch (e) {
      setCopyErr(e.message || "Could not copy this signal");
    } finally {
      setCopyBusy(null);
    }
  };

  // Live signal feed — every signal generated by anyone (manual or the AI auto-rotation)
  // streams in here instantly, no polling required.
  const onSignalMsg = useCallback((d) => {
    if (d?.type === "new_signal" && d.data) {
      const sig = d.data;
      setSigs((prev) => {
        if (prev.some((s) => s.id === sig.id)) return prev;
        return [sig, ...prev].slice(0, 30);
      });
      setNewIds((prev) => new Set(prev).add(sig.id));
      setTimeout(() => setNewIds((prev) => { const n = new Set(prev); n.delete(sig.id); return n; }), 8000);
    } else if (d?.type === "signal_closed" && d.data) {
      const { id, result, pnl_pips, close_price } = d.data;
      setSigs((prev) => prev.map((s) => s.id === id ? { ...s, status: "closed", result, pnl_pips, close_price } : s));
      setSel((prev) => prev && prev.id === id ? { ...prev, status: "closed", result, pnl_pips, close_price } : prev);
    }
  }, []);
  const liveStatus = useLiveSocket(SIGNALS_WS_URL, onSignalMsg);

  // Fetch chart when signal selected
  useEffect(() => {
    if (!sel) return;
    api.get(`/prices/chart?pair=${sel.pair}&timeframe=${sel.timeframe}&candles=80`)
      .then(d => setBars(d.candles || [])).catch(() => {});
  }, [sel, api]);

  const [delayInfo, setDelayInfo] = useState(null); // { plan_delay_minutes, realtime_signals_locked }

  // Load latest on mount; if Dashboard linked here with ?id=123, open that exact signal.
  useEffect(() => {
    api.get("/signals/latest?limit=20").then(d => {
      setSigs(d.signals || []);
      setDelayInfo({ delay: d.plan_delay_minutes || 0, locked: d.realtime_signals_locked || 0 });
      const wantId = searchParams.get("id");
      if (wantId) {
        const found = (d.signals || []).find(s => String(s.id) === wantId);
        if (found) { setSel(found); return; }
        api.get(`/signals/${wantId}`).then(s => setSel(s)).catch(() => {});
      }
    }).catch(() => {});
  }, []);

  const generate = async () => {
    setBusy(true); setGenErr("");
    try {
      const s = await api.post("/signals/generate", { pair, timeframe: tf });
      setSigs(p => [s, ...p.slice(0, 19)]);
      setSel(s);
      loadUsage();
    } catch (e) { setGenErr(e.message); }
    finally { setBusy(false); }
  };

  const bulk = async () => {
    setBusy(true); setGenErr("");
    try {
      const d = await api.post("/signals/bulk", { pairs: bulkP, timeframes: bulkTf, min_confidence: minConf, direction_filter: dirF });
      const list = d.signals || [];
      setSigs(list);
      if (list.length) setSel(list[0]);
      loadUsage();
    } catch (e) { setGenErr(e.message); }
    finally { setBusy(false); }
  };

  const loadHistory = async () => {
    setHBusy(true);
    try { const d = await api.get(`/signals/history?pair=${hPair}&timeframe=${hTf}&period=${hPer}`); setHData(d); }
    catch (e) { alert(e.message); }
    finally { setHBusy(false); }
  };

  // Signal detail panel
  const Detail = ({ s }) => {
    if (!s) return (
      <Card style={{ display: "flex", alignItems: "center", justifyContent: "center", minHeight: 300 }}>
        <span style={{ color: C.muted, fontSize: 13 }}>Select or generate a signal to view details</span>
      </Card>
    );
    const buy = s.direction === "BUY";
    const dc  = buy ? C.green : C.red;
    return (
      <Card>
        {/* Header */}
        <div style={{ display: "flex", justifyContent: "space-between", alignItems: "flex-start", marginBottom: 14 }}>
          <div>
            <div style={{ fontSize: 26, fontWeight: 800 }}>
              {s.pair} <span style={{ color: dc }}>{s.direction}</span>
            </div>
            <div style={{ fontSize: 11, color: C.muted, marginTop: 3 }}>{s.timeframe} · {s.entry_time}</div>
          </div>
          <div style={{ display: "flex", alignItems: "center", gap: 14 }}>
            {s.status === "closed" ? (
              <Badge col={s.result === "win" ? C.green : s.result === "loss" ? C.red : C.muted}>
                {(s.result || "").toUpperCase()} · {s.pnl_pips >= 0 ? "+" : ""}{f1(s.pnl_pips)} pips
              </Badge>
            ) : copiedIds.has(s.id) ? (
              <Badge col={C.green}>✓ Copied</Badge>
            ) : usage && usage.limits.copies_per_day != null && usage.usage.copies_today >= usage.limits.copies_per_day ? (
              <NavLink to="/billing" style={{ textDecoration: "none" }}>
                <Btn col={C.gold}>Daily copy limit reached — Upgrade</Btn>
              </NavLink>
            ) : (
              <div style={{ display: "flex", alignItems: "center", gap: 10 }}>
                {bridgeReady && (
                  <label style={{ display: "flex", alignItems: "center", gap: 5, fontSize: 10, color: C.muted, cursor: "pointer" }}>
                    <input type="checkbox" checked={executeLive} onChange={(e) => setExecuteLive(e.target.checked)} />
                    Execute live (MT5)
                  </label>
                )}
                <Btn col={C.gold} onClick={() => copySignal(s.id, s)} disabled={copyBusy === s.id}>
                  {copyBusy === s.id ? "Copying…" : executeLive && bridgeReady ? "↻ Copy — Live" : "↻ Copy This Signal"}
                </Btn>
              </div>
            )}
            <ConfRing val={s.confidence} size={60} />
          </div>
        </div>
        {lastCopied && lastCopied.pair === s.pair && (
          <div style={{ display: "flex", alignItems: "center", justifyContent: "space-between",
                        background: `${C.green}18`, border: `1px solid ${C.green}40`, borderRadius: 8,
                        padding: "9px 12px", marginBottom: 10, fontSize: 12 }}>
            <span>✓ Placed as a copy trade{lastCopied.mode === "mt5" ? " (sent to your MT5 bridge)" : " (simulated)"} — {lastCopied.pair} {lastCopied.direction}</span>
            <NavLink to="/copy" style={{ color: C.gold, fontWeight: 700, textDecoration: "none", whiteSpace: "nowrap", marginLeft: 10 }}>
              View in Copy Trading →
            </NavLink>
          </div>
        )}
        <ErrBox msg={copyErr} />

        {/* Metric grid */}
        <Grid cols="repeat(6,1fr)" gap={10} style={{ marginBottom: 13 }}>
          {[["ENTRY", fp(s.entry_price), C.text], ["STOP LOSS", fp(s.stop_loss), C.red], ["TAKE PROFIT", fp(s.take_profit), C.green],
            ["SL PIPS", f1(s.sl_pips), C.red], ["TP PIPS", f1(s.tp_pips), C.green], ["R:R", `1:${s.risk_reward}`, C.gold],
          ].map(([l, v, c]) => (
            <div key={l} style={{ background: C.surf2, border: `1px solid ${C.border}`, borderTop: `2px solid ${c}`, borderRadius: 8, padding: 10, textAlign: "center" }}>
              <div style={{ fontSize: 9, color: C.muted, letterSpacing: 0.5, marginBottom: 3 }}>{l}</div>
              <div style={{ fontSize: 14, fontWeight: 700, color: c, fontFamily: "monospace" }}>{v}</div>
            </div>
          ))}
        </Grid>

        {/* Chart */}
        <ChartWrap label={`${s.pair} · ${s.timeframe}  |  — EMA20 (gold)  — EMA50 (purple)  - - BB (blue)  ┈ S/R  ─ Trendline`}>
          <CandleChart1
            bars={bars}
            resetKey={`${s.id}_${s.pair}_${s.timeframe}`}
            pair={s.pair}
            entry={s.entry_price} sl={s.stop_loss} tp={s.take_profit}
            markers={s.markers || []}
            supportResistance={s.support_resistance || []}
            trendline={s.trendline || null}
          />
        </ChartWrap>

        {/* Indicators + AI side by side */}
        <Grid cols="1fr 1fr" gap={12}>
          <ChartWrap label="Technical Indicators">
            {[["RSI",     s.rsi,      0,   100],
              ["Stoch K", s.stoch_k,  0,   100],
              ["EMA 20",  s.ema20,    null, null],
              ["EMA 50",  s.ema50,    null, null],
              ["EMA 200", s.ema200,   null, null],
              ["BB Upper",s.bb_upper, null, null],
              ["BB Lower",s.bb_lower, null, null],
              ["ATR",     s.atr,      null, null],
            ].map(([l, v, lo, hi]) => (
              <div key={l} style={{ display: "flex", alignItems: "center", gap: 8, padding: "4px 0", borderBottom: `1px solid ${C.border}20`, fontSize: 11 }}>
                <span style={{ width: 68, color: C.muted, fontSize: 10, flexShrink: 0 }}>{l}</span>
                <span style={{ width: 72, textAlign: "right", fontFamily: "monospace", fontWeight: 600, flexShrink: 0 }}>{hi != null ? f1(v) : fp(v)}</span>
                {hi != null && (
                  <div style={{ flex: 1, height: 3, background: C.border, borderRadius: 2 }}>
                    <div style={{ width: `${Math.max(0, Math.min(100, ((v - lo) / (hi - lo)) * 100)).toFixed(0)}%`, height: "100%", background: C.gold, borderRadius: 2 }} />
                  </div>
                )}
              </div>
            ))}
          </ChartWrap>

          <ChartWrap label="AI Analysis">
            <div style={{ fontSize: 12, lineHeight: 1.75, color: C.text, opacity: 0.88, marginBottom: 13 }}>{s.ai_analysis}</div>
            <div style={{ fontSize: 9, color: C.muted, letterSpacing: 2, marginBottom: 6 }}>PATTERNS DETECTED</div>
            <div style={{ display: "flex", gap: 6, flexWrap: "wrap", marginBottom: 14 }}>
              <Badge col={dc}>{s.candle_pattern}</Badge>
              <Badge col={C.blue}>{s.chart_pattern}</Badge>
            </div>
            <div style={{ border: `1px solid ${dc}40`, background: dc + "0e", borderRadius: 8, padding: 13 }}>
              <div style={{ fontSize: 9, color: dc, letterSpacing: 2, marginBottom: 7 }}>RECOMMENDATION</div>
              <div style={{ fontSize: 12, lineHeight: 1.85 }}>
                <strong>{s.direction}</strong> {s.pair} at <strong style={{ color: C.gold, fontFamily: "monospace" }}>{fp(s.entry_price)}</strong><br />
                SL: <strong style={{ color: C.red, fontFamily: "monospace" }}>{fp(s.stop_loss)}</strong> ({f1(s.sl_pips)} pips) &nbsp;
                TP: <strong style={{ color: C.green, fontFamily: "monospace" }}>{fp(s.take_profit)}</strong> ({f1(s.tp_pips)} pips)<br />
                Best time: <strong style={{ color: C.gold }}>{s.entry_time}</strong>
              </div>
            </div>
          </ChartWrap>
        </Grid>
      </Card>
    );
  };

  return (
    <div style={{ padding: 20 }}>
      {/* Sub tabs */}
      <div style={{ display: "flex", gap: 8, marginBottom: 16 }}>
        <Btn col={subTab === "gen" ? C.gold : C.muted} ghost={subTab !== "gen"} onClick={() => setSubTab("gen")} style={{ fontSize: 11, padding: "6px 14px" }}>⚡ Generate</Btn>
        <Btn col={subTab === "history" ? C.gold : C.muted} ghost={subTab !== "history"} onClick={() => setSubTab("history")} style={{ fontSize: 11, padding: "6px 14px" }}>📊 History</Btn>
      </div>

      {delayInfo && delayInfo.delay > 0 && delayInfo.locked > 0 && (
        <div style={{ display: "flex", alignItems: "center", justifyContent: "space-between", gap: 10,
                      background: `${C.gold}14`, border: `1px solid ${C.gold}40`, borderRadius: 8,
                      padding: "9px 12px", marginBottom: 14, fontSize: 12, flexWrap: "wrap" }}>
          <span>🔒 {delayInfo.locked} real-time signal{delayInfo.locked === 1 ? "" : "s"} available now — Free plan shows signals {delayInfo.delay} min delayed.</span>
          <NavLink to="/billing" style={{ color: C.gold, fontWeight: 700, textDecoration: "none", whiteSpace: "nowrap" }}>Upgrade for real-time →</NavLink>
        </div>
      )}

      {subTab === "gen" && (
        <Grid cols="290px 1fr" gap={16}>
          {/* Left controls */}
          <div>
            <Card>
              <SectionTitle>Single Signal</SectionTitle>
              <FG label="Pair"><Sel value={pair} onChange={e => setPair(e.target.value)} options={FOREX_PAIRS} /></FG>
              <FG label="Timeframe"><Sel value={tf} onChange={e => setTf(e.target.value)} options={TFS} /></FG>
              <ErrBox msg={genErr} />
              {usage && usage.limits.signals_per_day != null && (
                <div style={{ fontSize: 10, color: C.muted, marginBottom: 8 }}>
                  {usage.usage.signals_today}/{usage.limits.signals_per_day} signals used today
                </div>
              )}
              {usage && usage.limits.signals_per_day != null && usage.usage.signals_today >= usage.limits.signals_per_day ? (
                <NavLink to="/billing" style={{ textDecoration: "none" }}>
                  <Btn col={C.gold} full>Daily limit reached — Upgrade to Pro</Btn>
                </NavLink>
              ) : (
                <Btn col={C.gold} full onClick={generate} disabled={busy}>{busy ? "Generating…" : "⚡ Generate Signal"}</Btn>
              )}
            </Card>

            <Card>
              <SectionTitle>Bulk Generate</SectionTitle>
              {usage && !usage.limits.bulk_generate ? (
                <div>
                  <div style={{ fontSize: 11, color: C.muted, marginBottom: 12, lineHeight: 1.7 }}>
                    Scanning multiple pairs/timeframes at once is a Pro feature.
                  </div>
                  <NavLink to="/billing" style={{ textDecoration: "none" }}>
                    <Btn col={C.gold} full>Upgrade to Unlock Bulk Generate</Btn>
                  </NavLink>
                </div>
              ) : (
                <>
                  <div style={{ fontSize: 9, color: C.muted, letterSpacing: 1.5, marginBottom: 6 }}>PAIRS</div>
                  <div style={{ display: "flex", flexWrap: "wrap", gap: 4, marginBottom: 10 }}>
                    {FOREX_PAIRS.map(p => <Pill key={p} on={bulkP.includes(p)} onClick={() => setBulkP(prev => prev.includes(p) ? prev.filter(x => x !== p) : [...prev, p])}>{p}</Pill>)}
                  </div>
                  <div style={{ fontSize: 9, color: C.muted, letterSpacing: 1.5, marginBottom: 6 }}>TIMEFRAMES</div>
                  <div style={{ display: "flex", gap: 4, marginBottom: 10 }}>
                    {TFS.map(t => <Pill key={t} on={bulkTf.includes(t)} onClick={() => setBulkTf(prev => prev.includes(t) ? prev.filter(x => x !== t) : [...prev, t])}>{t}</Pill>)}
                  </div>
                  <FG label="Direction">
                    <div style={{ display: "flex", gap: 4 }}>
                      {["ALL","BUY","SELL"].map(d => <Pill key={d} on={dirF === d} onClick={() => setDirF(d)}>{d}</Pill>)}
                    </div>
                  </FG>
                  <FG label={`Min Confidence: ${minConf}%`}>
                    <input type="range" min={0} max={90} step={5} value={minConf} onChange={e => setMinConf(+e.target.value)}
                      style={{ width: "100%", accentColor: C.gold }} />
                  </FG>
                  <Btn col={C.gold} ghost full onClick={bulk} disabled={busy}>{busy ? "Generating…" : "Generate All"}</Btn>
                </>
              )}
            </Card>

            <div style={{ display: "flex", alignItems: "center", gap: 6, margin: "2px 0 8px", fontSize: 10, color: C.muted }}>
              <span style={{
                width: 6, height: 6, borderRadius: "50%",
                background: liveStatus === "open" ? C.green : C.muted,
                boxShadow: liveStatus === "open" ? `0 0 6px ${C.green}` : "none",
              }} />
              {liveStatus === "open" ? "Live signal feed connected" : "Reconnecting to live feed…"}
            </div>
            <div style={{ maxHeight: 450, overflowY: "auto" }}>
              {sigs.map(s => (
                <div key={s.id} style={newIds.has(s.id) ? { boxShadow: `0 0 0 1px ${C.gold}`, borderRadius: 9, animation: "newSigPulse 1.6s ease-out 2" } : undefined}>
                  <SigCard s={s} selected={sel?.id === s.id} onClick={() => setSel(s)} />
                </div>
              ))}
            </div>
            <style>{`@keyframes newSigPulse{0%{box-shadow:0 0 0 1px ${C.gold}}50%{box-shadow:0 0 14px 2px ${C.gold}}100%{box-shadow:0 0 0 1px ${C.gold}}}`}</style>
          </div>

          {/* Right: detail */}
          <Detail s={sel} />
        </Grid>
      )}

      {subTab === "history" && (
        <Card>
          <SectionTitle>Signal History</SectionTitle>
          <div style={{ display: "flex", gap: 10, flexWrap: "wrap", marginBottom: 16, alignItems: "flex-end" }}>
            <div style={{ minWidth: 130 }}><FG label="Pair"><Sel value={hPair} onChange={e => setHPair(e.target.value)} options={FOREX_PAIRS} /></FG></div>
            <div style={{ minWidth: 100 }}><FG label="Timeframe"><Sel value={hTf} onChange={e => setHTf(e.target.value)} options={TFS} /></FG></div>
            <FG label="Period">
              <div style={{ display: "flex", gap: 5 }}>
                {["1M","3M","6M","1Y"].map(p => <Pill key={p} on={hPer === p} onClick={() => setHPer(p)}>{p}</Pill>)}
              </div>
            </FG>
            <Btn col={C.gold} onClick={loadHistory} disabled={hBusy}>{hBusy ? "Loading…" : "Load"}</Btn>
          </div>

          {hData && (
            <>
              <Grid cols="repeat(4,minmax(0,1fr))" gap={12} style={{ marginBottom: 16 }}>
                <Stat label="Total"      value={hData.count}                                                  color={C.blue} />
                <Stat label="BUY"        value={hData.signals?.filter(s => s.direction === "BUY").length || 0} color={C.green} />
                <Stat label="SELL"       value={hData.signals?.filter(s => s.direction === "SELL").length || 0} color={C.red} />
                <Stat label="Est. Win Rate" value={`${hData.estimated_winrate}%`}                             color={C.gold} />
              </Grid>
              <div style={{ display: "grid", gridTemplateColumns: "repeat(auto-fill,minmax(230px,1fr))", gap: 10 }}>
                {[...(hData.signals || [])].reverse().map((s, i) => (
                  <div key={i} style={{ background: C.surf2, border: `1px solid ${C.border}`, borderLeft: `3px solid ${s.direction === "BUY" ? C.green : C.red}`, borderRadius: 8, padding: 11 }}>
                    <div style={{ display: "flex", justifyContent: "space-between", marginBottom: 7 }}>
                      <div style={{ display: "flex", gap: 5 }}>
                        <strong>{s.pair}</strong>
                        <Badge col={s.direction === "BUY" ? C.green : C.red}>{s.direction}</Badge>
                      </div>
                      <span style={{ color: C.gold, fontWeight: 700 }}>{s.confidence}%</span>
                    </div>
                    <div style={{ display: "grid", gridTemplateColumns: "1fr 1fr 1fr", gap: 5, fontSize: 11 }}>
                      <div><div style={{ fontSize: 9, color: C.muted }}>ENTRY</div><span style={{ fontFamily: "monospace" }}>{fp(s.entry_price)}</span></div>
                      <div><div style={{ fontSize: 9, color: C.muted }}>SL</div><span style={{ color: C.red, fontFamily: "monospace" }}>{fp(s.stop_loss)}</span></div>
                      <div><div style={{ fontSize: 9, color: C.muted }}>TP</div><span style={{ color: C.green, fontFamily: "monospace" }}>{fp(s.take_profit)}</span></div>
                    </div>
                    <div style={{ display: "flex", gap: 5, marginTop: 7 }}>
                      <Badge col={{ STRONG: C.green, MODERATE: C.gold, WEAK: "#f97316", AVOID: C.red }[s.strength] || C.muted}>{s.strength}</Badge>
                      <Badge col={C.muted}>1:{s.risk_reward}</Badge>
                    </div>
                    <div style={{ fontSize: 9, color: C.muted, marginTop: 5 }}>{String(s.generated_at || s.expires_at || "").slice(0, 16)}</div>
                  </div>
                ))}
              </div>
            </>
          )}
        </Card>
      )}
    </div>
  );
}